function function_level_enid.length()
    return #function_level_enid._data
end

-- 
function function_level_enid.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function function_level_enid.indexOf(index)
    if index == nil or not function_level_enid._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/function_level_enid.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "function_level_enid" )
        return setmetatable({_raw = function_level_enid._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = function_level_enid._data[index]}, mt)
end

--
function function_level_enid.get(function_id)
    
    return function_level_enid.indexOf(__index_function_id[function_id])
        
end

