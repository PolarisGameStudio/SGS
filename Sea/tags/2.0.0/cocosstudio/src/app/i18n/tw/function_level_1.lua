    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "function_level_1" )
        return setmetatable({_raw = function_level_1._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
