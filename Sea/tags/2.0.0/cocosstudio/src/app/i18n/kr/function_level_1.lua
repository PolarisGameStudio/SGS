function function_level_1.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function function_level_1.indexOf(index)
    if index == nil or not function_level_1._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/function_level_1.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "function_level_1" )
        return setmetatable({_raw = function_level_1._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = function_level_1._data[index]}, mt)
end

