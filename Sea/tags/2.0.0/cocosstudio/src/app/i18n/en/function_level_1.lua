-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in function_level_en")
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[__key_map[k]]
    end
}

-- 
function function_level_en.length()
    return #function_level_en._data
end

-- 
function function_level_en.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function function_level_en.indexOf(index)
    if index == nil or not function_level_en._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/function_level_en.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "function_level_en" )
        return setmetatable({_raw = function_level_en._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = function_level_en._data[index]}, mt)
end

--
function function_level_en.get(function_id)
    
    return function_level_en.indexOf(__index_function_id[function_id])
        
end

--
function function_level_en.set(function_id, key, value)
    local record = function_level_en.get(function_id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function function_level_en.index()
    return __index_function_id
end

