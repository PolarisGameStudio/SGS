function equipment_active_drop.length()
    return #equipment_active_drop._data
end

-- 
function equipment_active_drop.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function equipment_active_drop.indexOf(index)
    if index == nil or not equipment_active_drop._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/equipment_active_drop.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "equipment_active_drop" )
        return setmetatable({_raw = equipment_active_drop._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = equipment_active_drop._data[index]}, mt)
end

--
function equipment_active_drop.get(order)
    
    return equipment_active_drop.indexOf(__index_order[order])
        
end

