-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in hero_friend")
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[__key_map[k]]
    end
}

-- 
function hero_friend.length()
    return #hero_friend._data
end

-- 
function hero_friend.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function hero_friend.indexOf(index)
    if index == nil or not hero_friend._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/hero_friend.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "hero_friend" )
        return setmetatable({_raw = hero_friend._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = hero_friend._data[index]}, mt)
end

--
function hero_friend.get(friend_id)
    
    return hero_friend.indexOf(__index_friend_id[friend_id])
        
end

--
function hero_friend.set(friend_id, key, value)
    local record = hero_friend.get(friend_id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function hero_friend.index()
    return __index_friend_id
end

return hero_friend