-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in hero_fate")
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[__key_map[k]]
    end
}

-- 
function hero_fate.length()
    return #hero_fate._data
end

-- 
function hero_fate.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function hero_fate.indexOf(index)
    if index == nil or not hero_fate._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/hero_fate.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "hero_fate" )
        return setmetatable({_raw = hero_fate._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = hero_fate._data[index]}, mt)
end

--
function hero_fate.get(fate_id)
    
    return hero_fate.indexOf(__index_fate_id[fate_id])
        
end

--
function hero_fate.set(fate_id, key, value)
    local record = hero_fate.get(fate_id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function hero_fate.index()
    return __index_fate_id
end

return hero_fate