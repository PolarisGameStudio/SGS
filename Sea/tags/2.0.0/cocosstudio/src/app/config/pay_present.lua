-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in pay_present")
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[__key_map[k]]
    end
}

-- 
function pay_present.length()
    return #pay_present._data
end

-- 
function pay_present.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function pay_present.indexOf(index)
    if index == nil or not pay_present._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/pay_present.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "pay_present" )
        return setmetatable({_raw = pay_present._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = pay_present._data[index]}, mt)
end

--
function pay_present.get(id)
    
    return pay_present.indexOf(__index_id[id])
        
end

--
function pay_present.set(id, key, value)
    local record = pay_present.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function pay_present.index()
    return __index_id
end

return pay_present