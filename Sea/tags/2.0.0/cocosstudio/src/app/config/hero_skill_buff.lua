function hero_skill_buff.indexOf(index)
    if index == nil or not hero_skill_buff._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/hero_skill_buff.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "hero_skill_buff" )
        return setmetatable({_raw = hero_skill_buff._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = hero_skill_buff._data[index]}, mt)
end

