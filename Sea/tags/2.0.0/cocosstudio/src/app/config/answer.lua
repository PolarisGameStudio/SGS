-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in answer")
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[__key_map[k]]
    end
}

-- 
function answer.length()
    return #answer._data
end

-- 
function answer.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function answer.indexOf(index)
    if index == nil or not answer._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/answer.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "answer" )
        return setmetatable({_raw = answer._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = answer._data[index]}, mt)
end

--
function answer.get(id)
    
    return answer.indexOf(__index_id[id])
        
end

--
function answer.set(id, key, value)
    local record = answer.get(id)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function answer.index()
    return __index_id
end

return answer