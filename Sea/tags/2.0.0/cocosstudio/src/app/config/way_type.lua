function way_type.length()
    return #way_type._data
end

-- 
function way_type.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function way_type.indexOf(index)
    if index == nil or not way_type._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/way_type.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "way_type" )
        return setmetatable({_raw = way_type._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = way_type._data[index]}, mt)
end

