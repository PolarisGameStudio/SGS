-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in instrument_level")
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[__key_map[k]]
    end
}

-- 
function instrument_level.length()
    return #instrument_level._data
end

-- 
function instrument_level.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function instrument_level.indexOf(index)
    if index == nil or not instrument_level._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/instrument_level.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "instrument_level" )
        return setmetatable({_raw = instrument_level._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = instrument_level._data[index]}, mt)
end

--
function instrument_level.get(level,rank_type)
    
    local k = level .. '_' .. rank_type
    return instrument_level.indexOf(__index_level_rank_type[k])
        
end

--
function instrument_level.set(level,rank_type, key, value)
    local record = instrument_level.get(level,rank_type)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function instrument_level.index()
    return __index_level_rank_type
end

return instrument_level