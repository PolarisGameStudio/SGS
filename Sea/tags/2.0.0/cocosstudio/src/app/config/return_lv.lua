-- metatable
local mt = { 
    __index = function(t, k) 
        assert(__key_map[k], "cannot find " .. k .. " in return_lv")
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[__key_map[k]]
    end
}

-- 
function return_lv.length()
    return #return_lv._data
end

-- 
function return_lv.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function return_lv.indexOf(index)
    if index == nil or not return_lv._data[index] then
        return nil
    end
    if Lang.lang ~= "cn" and Lang.lang ~= _lang then 
        _lang = Lang.lang 
        if Lang.isFileExist("app/i18n/".. _lang .."/config/return_lv.lua") then _isExist =  true end
    end
    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "return_lv" )
        return setmetatable({_raw = return_lv._data[index], _lang=table._data[index], _lang_key_map=table.__key_map}, mt)
    end
    return setmetatable({_raw = return_lv._data[index]}, mt)
end

--
function return_lv.get(lv_standard)
    
    return return_lv.indexOf(__index_lv_standard[lv_standard])
        
end

--
function return_lv.set(lv_standard, key, value)
    local record = return_lv.get(lv_standard)
    if record then
        local keyIndex = __key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function return_lv.index()
    return __index_lv_standard
end

return return_lv