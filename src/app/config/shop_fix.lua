-- metatable
local mt = { 
    __index = function(t, k) 
        local key_map = t._raw_key_map
        assert(key_map[k], "cannot find " .. k .. " in shop_fix")
        if _lang ~= "cn" and _isDataExist  and t._data_key_map[k] then
            return t._data[t._data_key_map[k]]
        end
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[key_map[k]]
    end
}

-- 
function shop_fix.length()
    return #shop_fix._data
end

-- 
function shop_fix.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function shop_fix.isVersionValid(v)
    if shop_fix.version then
        if v then
            return shop_fix.version <= v
        else
            return false
        end
    else
        return true
    end
end

--
function shop_fix.indexOf(index)
    if index == nil or not shop_fix._data[index] then
        return nil
    end
    if Lang.lang ~= _lang then 
        _lang = Lang.lang
        if Lang.lang ~= "cn" then
            _isDataExist = Lang.isFileExist("app/i18n/".. _lang .."/data/shop_fix.lua")
            _isExist = Lang.isFileExist("app/i18n/".. _lang .."/config/shop_fix.lua")
            _isBaseExist =  Lang.isFileExist("app/i18n/".. _lang .."/base/shop_fix.lua")

            if _isDataExist then
                local table = require( "app.i18n.".. _lang ..".data." .. "shop_fix" )
                _isDataExist = shop_fix.isVersionValid(table.version)
            end
            if _isBaseExist then
                local table = require( "app.i18n.".. _lang ..".base." .. "shop_fix" )
                _isBaseExist = shop_fix.isVersionValid(table.version)
            end
            if _isExist then
                local table = require( "app.i18n.".. _lang ..".config." .. "shop_fix" )
                _isExist = shop_fix.isVersionValid(table.version)
            end
        end
    end
    local config = {_raw = nil,_raw_key_map = __key_map,_lang = nil,_lang_key_map = nil,_data = nil,_data_key_map = nil}
    if _lang ~= "cn" and _isDataExist then
        local table = require( "app.i18n.".. _lang ..".data." .. "shop_fix" )
        local main_key = __main_key_map[index]
		local index_key = "__index_id"
        local data_index = table[index_key][main_key]
        config._data = table._data[data_index]
        config._data_key_map = table.__key_map
    end

    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "shop_fix" )
        local main_key = __main_key_map[index]
		local index_key = "__index_id"
        local lang_index = table[index_key][main_key]
        config._lang = table._data[lang_index]
        config._lang_key_map = table.__key_map
    end
    config._raw = shop_fix._data[index]
    if _lang ~= "cn" and _isBaseExist then
        local table_base = require( "app.i18n.".. _lang ..".base." .. "shop_fix" )
        config._raw = table_base._data[index] 
    end
    return setmetatable(config, mt)
end

--
function shop_fix.get(id)
    
    return shop_fix.indexOf(__index_id[id])
        
end

--
function shop_fix.set(id, key, value)
    local record = shop_fix.get(id)
    if record then
        if _lang ~= "cn" and _isDataExist then
            local keyIndex =  record._data_key_map[key]
            if keyIndex then
                record._data[keyIndex] = value
                return
            end
        end
        if _lang ~= "cn" and _isExist then
            local keyIndex =  record._lang_key_map[key]
            if keyIndex then
                record._lang[keyIndex] = value
                return
            end
        end
        local keyIndex = record._raw_key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function shop_fix.index()
    return __index_id
end

