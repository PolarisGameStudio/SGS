-- metatable
local mt = { 
    __index = function(t, k) 
        local key_map = t._raw_key_map
        assert(key_map[k], "cannot find " .. k .. " in drop")
        if _lang ~= "cn" and _isDataExist  and t._data_key_map[k] then
            return t._data[t._data_key_map[k]]
        end
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[key_map[k]]
    end
}

-- 
function drop.length()
    return #drop._data
end

-- 
function drop.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function drop.isVersionValid(v)
    if drop.version then
        if v then
            return drop.version <= v
        else
            return false
        end
    else
        return true
    end
end

--
function drop.indexOf(index)
    if index == nil or not drop._data[index] then
        return nil
    end
    if Lang.lang ~= _lang then 
        _lang = Lang.lang
        if Lang.lang ~= "cn" then
            _isDataExist = Lang.isFileExist("app/i18n/".. _lang .."/data/drop.lua")
            _isExist = Lang.isFileExist("app/i18n/".. _lang .."/config/drop.lua")
            _isBaseExist =  Lang.isFileExist("app/i18n/".. _lang .."/base/drop.lua")

            if _isDataExist then
                local table = require( "app.i18n.".. _lang ..".data." .. "drop" )
                _isDataExist = drop.isVersionValid(table.version)
            end
            if _isBaseExist then
                local table = require( "app.i18n.".. _lang ..".base." .. "drop" )
                _isBaseExist = drop.isVersionValid(table.version)
            end
            if _isExist then
                local table = require( "app.i18n.".. _lang ..".config." .. "drop" )
                _isExist = drop.isVersionValid(table.version)
            end
        end
    end
    local config = {_raw = nil,_raw_key_map = __key_map,_lang = nil,_lang_key_map = nil,_data = nil,_data_key_map = nil}
    if _lang ~= "cn" and _isDataExist then
        local table = require( "app.i18n.".. _lang ..".data." .. "drop" )
        local main_key = __main_key_map[index]
		local index_key = "__index_drop_id"
        local data_index = table[index_key][main_key]
        config._data = table._data[data_index]
        config._data_key_map = table.__key_map
    end

    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "drop" )
        local main_key = __main_key_map[index]
		local index_key = "__index_drop_id"
        local lang_index = table[index_key][main_key]
        config._lang = table._data[lang_index]
        config._lang_key_map = table.__key_map
    end
    config._raw = drop._data[index]
    if _lang ~= "cn" and _isBaseExist then
        local table_base = require( "app.i18n.".. _lang ..".base." .. "drop" )
        config._raw = table_base._data[index] 
    end
    return setmetatable(config, mt)
end

--
function drop.get(drop_id)
    
    return drop.indexOf(__index_drop_id[drop_id])
        
end

--
function drop.set(drop_id, key, value)
    local record = drop.get(drop_id)
    if record then
        if _lang ~= "cn" and _isDataExist then
            local keyIndex =  record._data_key_map[key]
            if keyIndex then
                record._data[keyIndex] = value
                return
            end
        end
        if _lang ~= "cn" and _isExist then
            local keyIndex =  record._lang_key_map[key]
            if keyIndex then
                record._lang[keyIndex] = value
                return
            end
        end
        local keyIndex = record._raw_key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function drop.index()
    return __index_drop_id
end

