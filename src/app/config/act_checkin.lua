-- metatable
local mt = { 
    __index = function(t, k) 
        local key_map = t._raw_key_map
        assert(key_map[k], "cannot find " .. k .. " in act_checkin")
        if _lang ~= "cn" and _isDataExist  and t._data_key_map[k] then
            return t._data[t._data_key_map[k]]
        end
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[key_map[k]]
    end
}

-- 
function act_checkin.length()
    return #act_checkin._data
end

-- 
function act_checkin.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function act_checkin.isVersionValid(v)
    if act_checkin.version then
        if v then
            return act_checkin.version <= v
        else
            return false
        end
    else
        return true
    end
end

--
function act_checkin.indexOf(index)
    if index == nil or not act_checkin._data[index] then
        return nil
    end
    if Lang.lang ~= _lang then 
        _lang = Lang.lang
        if Lang.lang ~= "cn" then
            _isDataExist = Lang.isFileExist("app/i18n/".. _lang .."/data/act_checkin.lua")
            _isExist = Lang.isFileExist("app/i18n/".. _lang .."/config/act_checkin.lua")
            _isBaseExist =  Lang.isFileExist("app/i18n/".. _lang .."/base/act_checkin.lua")

            if _isDataExist then
                local table = require( "app.i18n.".. _lang ..".data." .. "act_checkin" )
                _isDataExist = act_checkin.isVersionValid(table.version)
            end
            if _isBaseExist then
                local table = require( "app.i18n.".. _lang ..".base." .. "act_checkin" )
                _isBaseExist = act_checkin.isVersionValid(table.version)
            end
            if _isExist then
                local table = require( "app.i18n.".. _lang ..".config." .. "act_checkin" )
                _isExist = act_checkin.isVersionValid(table.version)
            end
        end
    end
    local config = {_raw = nil,_raw_key_map = __key_map,_lang = nil,_lang_key_map = nil,_data = nil,_data_key_map = nil}
    if _lang ~= "cn" and _isDataExist then
        local table = require( "app.i18n.".. _lang ..".data." .. "act_checkin" )
        local main_key = __main_key_map[index]
		local index_key = "__index_month_day"
        local data_index = table[index_key][main_key]
        config._data = table._data[data_index]
        config._data_key_map = table.__key_map
    end

    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "act_checkin" )
        local main_key = __main_key_map[index]
		local index_key = "__index_month_day"
        local lang_index = table[index_key][main_key]
        config._lang = table._data[lang_index]
        config._lang_key_map = table.__key_map
    end
    config._raw = act_checkin._data[index]
    if _lang ~= "cn" and _isBaseExist then
        local table_base = require( "app.i18n.".. _lang ..".base." .. "act_checkin" )
        config._raw = table_base._data[index] 
    end
    return setmetatable(config, mt)
end

--
function act_checkin.get(month,day)
    
    local k = month .. '_' .. day
    return act_checkin.indexOf(__index_month_day[k])
        
end

--
function act_checkin.set(month,day, key, value)
    local record = act_checkin.get(month,day)
    if record then
        if _lang ~= "cn" and _isDataExist then
            local keyIndex =  record._data_key_map[key]
            if keyIndex then
                record._data[keyIndex] = value
                return
            end
        end
        if _lang ~= "cn" and _isExist then
            local keyIndex =  record._lang_key_map[key]
            if keyIndex then
                record._lang[keyIndex] = value
                return
            end
        end
        local keyIndex = record._raw_key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function act_checkin.index()
    return __index_month_day
end

