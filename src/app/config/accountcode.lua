-- metatable
local mt = { 
    __index = function(t, k) 
        local key_map = t._raw_key_map
        assert(key_map[k], "cannot find " .. k .. " in accountcode")
        if _lang ~= "cn" and _isDataExist  and t._data_key_map[k] then
            return t._data[t._data_key_map[k]]
        end
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[key_map[k]]
    end
}

-- 
function accountcode.length()
    return #accountcode._data
end

-- 
function accountcode.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function accountcode.isVersionValid(v)
    if accountcode.version then
        if v then
            return accountcode.version <= v
        else
            return false
        end
    else
        return true
    end
end

--
function accountcode.indexOf(index)
    if index == nil or not accountcode._data[index] then
        return nil
    end
    if Lang.lang ~= _lang then 
        _lang = Lang.lang
        if Lang.lang ~= "cn" then
            _isDataExist = Lang.isFileExist("app/i18n/".. _lang .."/data/accountcode.lua")
            _isExist = Lang.isFileExist("app/i18n/".. _lang .."/config/accountcode.lua")
            _isBaseExist =  Lang.isFileExist("app/i18n/".. _lang .."/base/accountcode.lua")

            if _isDataExist then
                local table = require( "app.i18n.".. _lang ..".data." .. "accountcode" )
                _isDataExist = accountcode.isVersionValid(table.version)
            end
            if _isBaseExist then
                local table = require( "app.i18n.".. _lang ..".base." .. "accountcode" )
                _isBaseExist = accountcode.isVersionValid(table.version)
            end
            if _isExist then
                local table = require( "app.i18n.".. _lang ..".config." .. "accountcode" )
                _isExist = accountcode.isVersionValid(table.version)
            end
        end
    end
    local config = {_raw = nil,_raw_key_map = __key_map,_lang = nil,_lang_key_map = nil,_data = nil,_data_key_map = nil}
    if _lang ~= "cn" and _isDataExist then
        local table = require( "app.i18n.".. _lang ..".data." .. "accountcode" )
        local main_key = __main_key_map[index]
		local index_key = "__index_acode"
        local data_index = table[index_key][main_key]
        config._data = table._data[data_index]
        config._data_key_map = table.__key_map
    end

    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "accountcode" )
        local main_key = __main_key_map[index]
		local index_key = "__index_acode"
        local lang_index = table[index_key][main_key]
        config._lang = table._data[lang_index]
        config._lang_key_map = table.__key_map
    end
    config._raw = accountcode._data[index]
    if _lang ~= "cn" and _isBaseExist then
        local table_base = require( "app.i18n.".. _lang ..".base." .. "accountcode" )
        config._raw = table_base._data[index] 
    end
    return setmetatable(config, mt)
end

--
function accountcode.get(acode)
    
    return accountcode.indexOf(__index_acode[acode])
        
end

--
function accountcode.set(acode, key, value)
    local record = accountcode.get(acode)
    if record then
        if _lang ~= "cn" and _isDataExist then
            local keyIndex =  record._data_key_map[key]
            if keyIndex then
                record._data[keyIndex] = value
                return
            end
        end
        if _lang ~= "cn" and _isExist then
            local keyIndex =  record._lang_key_map[key]
            if keyIndex then
                record._lang[keyIndex] = value
                return
            end
        end
        local keyIndex = record._raw_key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function accountcode.index()
    return __index_acode
end

