-- metatable
local mt = { 
    __index = function(t, k) 
        local key_map = t._raw_key_map
        assert(key_map[k], "cannot find " .. k .. " in function_level_1")
        if _lang ~= "cn" and _isDataExist  and t._data_key_map[k] then
            return t._data[t._data_key_map[k]]
        end
        if _lang ~= "cn" and _isExist  and t._lang_key_map[k] then
            return t._lang[t._lang_key_map[k]]
        end
        return t._raw[key_map[k]]
    end
}

-- 
function function_level_1.length()
    return #function_level_1._data
end

-- 
function function_level_1.hasKey(k)
    if __key_map[k] == nil then
        return false
    else
        return true
    end
end

--
function function_level_1.isVersionValid(v)
    if function_level_1.version then
        if v then
            return function_level_1.version <= v
        else
            return false
        end
    else
        return true
    end
end

--
function function_level_1.indexOf(index)
    if index == nil or not function_level_1._data[index] then
        return nil
    end
    if Lang.lang ~= _lang then 
        _lang = Lang.lang
        if Lang.lang ~= "cn" then
            _isDataExist = Lang.isFileExist("app/i18n/".. _lang .."/data/function_level_1.lua")
            _isExist = Lang.isFileExist("app/i18n/".. _lang .."/config/function_level_1.lua")
            _isBaseExist =  Lang.isFileExist("app/i18n/".. _lang .."/base/function_level_1.lua")

            if _isDataExist then
                local table = require( "app.i18n.".. _lang ..".data." .. "function_level_1" )
                _isDataExist = function_level_1.isVersionValid(table.version)
            end
            if _isBaseExist then
                local table = require( "app.i18n.".. _lang ..".base." .. "function_level_1" )
                _isBaseExist = function_level_1.isVersionValid(table.version)
            end
            if _isExist then
                local table = require( "app.i18n.".. _lang ..".config." .. "function_level_1" )
                _isExist = function_level_1.isVersionValid(table.version)
            end
        end
    end
    local config = {_raw = nil,_raw_key_map = __key_map,_lang = nil,_lang_key_map = nil,_data = nil,_data_key_map = nil}
    if _lang ~= "cn" and _isDataExist then
        local table = require( "app.i18n.".. _lang ..".data." .. "function_level_1" )
        local main_key = __main_key_map[index]
		local index_key = "__index_function_id"
        local data_index = table[index_key][main_key]
        config._data = table._data[data_index]
        config._data_key_map = table.__key_map
    end

    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "function_level_1" )
        local main_key = __main_key_map[index]
		local index_key = "__index_function_id"
        local lang_index = table[index_key][main_key]
        config._lang = table._data[lang_index]
        config._lang_key_map = table.__key_map
    end
    config._raw = function_level_1._data[index]
    if _lang ~= "cn" and _isBaseExist then
        local table_base = require( "app.i18n.".. _lang ..".base." .. "function_level_1" )
        config._raw = table_base._data[index] 
    end
    return setmetatable(config, mt)
end

--
function function_level_1.get(function_id)
    
    return function_level_1.indexOf(__index_function_id[function_id])
        
end

--
function function_level_1.set(function_id, key, value)
    local record = function_level_1.get(function_id)
    if record then
        if _lang ~= "cn" and _isDataExist then
            local keyIndex =  record._data_key_map[key]
            if keyIndex then
                record._data[keyIndex] = value
                return
            end
        end
        if _lang ~= "cn" and _isExist then
            local keyIndex =  record._lang_key_map[key]
            if keyIndex then
                record._lang[keyIndex] = value
                return
            end
        end
        local keyIndex = record._raw_key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function function_level_1.index()
    return __index_function_id
end

