    end
    if Lang.lang ~= _lang then 
        _lang = Lang.lang
        if Lang.lang ~= "cn" then
            _isDataExist = Lang.isFileExist("app/i18n/".. _lang .."/data/instrument_level.lua")
            _isExist = Lang.isFileExist("app/i18n/".. _lang .."/config/instrument_level.lua")
            _isBaseExist =  Lang.isFileExist("app/i18n/".. _lang .."/base/instrument_level.lua")

            if _isDataExist then
                local table = require( "app.i18n.".. _lang ..".data." .. "instrument_level" )
                _isDataExist = instrument_level.isVersionValid(table.version)
            end
            if _isBaseExist then
                local table = require( "app.i18n.".. _lang ..".base." .. "instrument_level" )
                _isBaseExist = instrument_level.isVersionValid(table.version)
            end
            if _isExist then
                local table = require( "app.i18n.".. _lang ..".config." .. "instrument_level" )
                _isExist = instrument_level.isVersionValid(table.version)
            end
        end
    end
    local config = {_raw = nil,_raw_key_map = __key_map,_lang = nil,_lang_key_map = nil,_data = nil,_data_key_map = nil}
    if _lang ~= "cn" and _isDataExist then
        local table = require( "app.i18n.".. _lang ..".data." .. "instrument_level" )
        local main_key = __main_key_map[index]
		local index_key = "__index_level_rank_type"
        local data_index = table[index_key][main_key]
        config._data = table._data[data_index]
        config._data_key_map = table.__key_map
    end

    if _lang ~= "cn" and _isExist then
        local table = require( "app.i18n.".. _lang ..".config." .. "instrument_level" )
        local main_key = __main_key_map[index]
		local index_key = "__index_level_rank_type"
        local lang_index = table[index_key][main_key]
        config._lang = table._data[lang_index]
        config._lang_key_map = table.__key_map
    end
    config._raw = instrument_level._data[index]
    if _lang ~= "cn" and _isBaseExist then
        local table_base = require( "app.i18n.".. _lang ..".base." .. "instrument_level" )
        config._raw = table_base._data[index] 
    end
    return setmetatable(config, mt)
end

--
function instrument_level.get(level,rank_type)
    
    local k = level .. '_' .. rank_type
    return instrument_level.indexOf(__index_level_rank_type[k])
        
end

--
function instrument_level.set(level,rank_type, key, value)
    local record = instrument_level.get(level,rank_type)
    if record then
        if _lang ~= "cn" and _isDataExist then
            local keyIndex =  record._data_key_map[key]
            if keyIndex then
                record._data[keyIndex] = value
                return
            end
        end
        if _lang ~= "cn" and _isExist then
            local keyIndex =  record._lang_key_map[key]
            if keyIndex then
                record._lang[keyIndex] = value
                return
            end
        end
        local keyIndex = record._raw_key_map[key]
        if keyIndex then
            record._raw[keyIndex] = value
        end
    end
end

--
function instrument_level.index()
    return __index_level_rank_type
end

return instrument_level